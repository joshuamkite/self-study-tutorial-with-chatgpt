for a in sys.argv[1:]:
    x = a.split(" ")
    d = int(x[0].replace("-", ""))
    g = ({k: v for (k, v) in dict.items(D) if k <= d})
    z = max(zip(g.keys(), g.values()))[1]
    p = int(x[1].replace(":", ""))
    t = ({k: v for (k, v) in dict.items(N) if k <= p})
    u = max(zip(t.keys(), t.values()))[1]
    w = {Y[i]: u[i] for i in range(len(u))}
    s = (w[z])
    if s == z:
        print(z)
    else:
        print(f"{z}{s}")
